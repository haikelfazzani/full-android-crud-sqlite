package com.example.haikel.tpsqlite.model;

public class Post {
    private int userId, id;
    private String title, body;

    public Post() {}

    public int getUserId() {
        return userId;
    }

    public int getId() {
        return id;
    }

    public String getTitle() {
        return title;
    }

    public String getBody() {
        return body;
    }

}
